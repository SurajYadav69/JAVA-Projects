public class MathExpression {
    public static void main(String[] args) {
        int numerator1 = 7;
        int denominator1 = 4;
        int numerator2 = 9;
        int denominator2 = 2;

        // Calculate the product of the fractions
        int productNumerator = numerator1 * numerator2;
        int productDenominator = denominator1 * denominator2;

        System.out.println("Result: " + productNumerator + "/" + productDenominator);
    }
}
